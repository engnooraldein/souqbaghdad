// UI component library files (shadcn/radix) — add minimal docs
const uiComponentDirs = ['src/components/ui'];
const genericUiHeader = (name) => `// ===========================================
// مسؤولية هذا الملف:
// مكوّن UI من مكتبة shadcn/ui (${name}).
//
// لا يتصل بـ Supabase. مكوّن UI بحت.
// تم توليده تلقائياً ويُنصح بعدم تعديله مباشرة.
// استخدمه عبر الـ Props الموثقة في shadcn/ui.
// ===========================================
`;

const BASE = 'src/components';

let count = 0;
let skipped = 0;

// Document app-specific components
for (const [filename, doc] of Object.entries(componentDocs)) {
  const filePath = path.join(BASE, filename);
  if (!fs.existsSync(filePath)) {
    console.warn(`⚠️  File not found: ${filePath}`);
    continue;
  }
  
  let content = fs.readFileSync(filePath, 'utf-8').replace(/\r\n/g, '\n');
  
  if (content.includes('// ===========================================')) {
    skipped++;
    continue;
  }
  
  content = doc.header + content;
  fs.writeFileSync(filePath, content.replace(/\n/g, '\r\n'), 'utf-8');
  console.log(`✅ ${filename}`);
  count++;
}

// Document UI library components with minimal header
const uiDir = 'src/components/ui';
if (fs.existsSync(uiDir)) {
  const uiFiles = fs.readdirSync(uiDir).filter(f => f.endsWith('.tsx') || f.endsWith('.ts'));
  for (const file of uiFiles) {
    const filePath = path.join(uiDir, file);
    let content = fs.readFileSync(filePath, 'utf-8').replace(/\r\n/g, '\n');
    
    if (content.includes('// ===========================================')) {
      skipped++;
      continue;
    }
    
    const name = file.replace(/\.(tsx|ts)$/, '');
    content = genericUiHeader(name) + content;
    fs.writeFileSync(filePath, content.replace(/\n/g, '\r\n'), 'utf-8');
    count++;
  }
  console.log(`✅ Documented ${uiFiles.length} UI library files`);
}

console.log(`\n✅ Done! ${count} documented, ${skipped} already had docs.`);
